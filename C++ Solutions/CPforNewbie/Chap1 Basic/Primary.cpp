#include<bits/stdc++.h>

using namespace std;
 
int main()
{
   cout << "Size of int: " << sizeof(int)
     << " bytes" << endl;
   cout << "Size of long: " << sizeof(long)
      << " bytes" << endl;
   cout << "Size of long long: " << sizeof(unsigned long long)
      << " bytes" << endl;
   cout << "Size of double: " << sizeof(double)
      << " bytes" << endl;
   cout << "Size of long double: " << sizeof(long double)
      << " bytes" << endl;
   cout << "Size of char: " << sizeof(char)
      << " byte" << endl;
   cout << "Size of string: " << sizeof(string)
      << " bytes" <<endl;
   
   return 0;
}